BASE_DIR = Path(__file__).resolve().parent
BACKUP_DIR = Path("backups")

def create_backup():
    """Erstellt timestamped Backup aller wichtigen Dateien"""
    
    # Timestamp f√ºr eindeutige Backup-Namen
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    backup_folder = BACKUP_DIR / timestamp
    
    # Erstelle Backup-Ordner
    backup_folder.mkdir(parents=True, exist_ok=True)
    
    print(f"üì¶ Erstelle Backup: {backup_folder}")
    print(f"   Sichere {len(FILES_TO_BACKUP)} Dateien...\n")
    
    backed_up = 0
    skipped = []
    
    for file_path in FILES_TO_BACKUP:
        source = BASE_DIR / file_path
        
        if not source.exists():
            skipped.append(file_path)
            continue
        
        # Behalte Ordnerstruktur bei
        dest = backup_folder / file_path
        dest.parent.mkdir(parents=True, exist_ok=True)
        
        # Kopiere Datei
        shutil.copy2(source, dest)
        size_kb = source.stat().st_size / 1024
        print(f"‚úÖ {file_path} ({size_kb:.1f} KB)")
        backed_up += 1
    
    # Erstelle Info-Datei
    info_file = backup_folder / "backup_info.txt"
    with open(info_file, "w", encoding="utf-8") as f:
        f.write(f"‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n")
        f.write(f"  FUNDING BOT BACKUP\n")
        f.write(f"‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê‚ïê\n")
        f.write(f"Erstellt: {datetime.now().strftime('%Y-%m-%d %H:%M:%S')}\n")
        f.write(f"Dateien gesichert: {backed_up}\n")
        f.write(f"Dateien √ºbersprungen: {len(skipped)}\n\n")
        f.write(f"Gesicherte Dateien:\n")
        for file in FILES_TO_BACKUP:
            if file not in skipped:
                f.write(f"  ‚úÖ {file}\n")
        if skipped:
            f.write(f"\n√úbersprungen (nicht gefunden):\n")
            for file in skipped:
                f.write(f"  ‚ö†Ô∏è {file}\n")
    
    # Berechne Gesamtgr√∂√üe
    total_size = sum(f.stat().st_size for f in backup_folder.rglob('*') if f.is_file())
    total_mb = total_size / 1024 / 1024
    
    print(f"\n{'‚îÄ' * 50}")
    print(f"‚úÖ Backup abgeschlossen: {backed_up} Dateien ({total_mb:.2f} MB)")
    
    if skipped:
        print(f"‚ö†Ô∏è  √úbersprungen: {len(skipped)} Dateien (nicht gefunden)")
    
    print(f"üìç Speicherort: {backup_folder.absolute()}")
    
    # Zeige die letzten Backups
    list_recent_backups()

def list_recent_backups(n=5):
    """Listet die letzten N Backups auf"""
    if not BACKUP_DIR.exists():
        print("üì≠ Keine Backups vorhanden")
        return
    
    backups = sorted([d for d in BACKUP_DIR.iterdir() if d.is_dir()], reverse=True)[:n]
    
    if backups:
        print(f"\nüìö Letzte {len(backups)} Backups:")
        for i, backup in enumerate(backups, 1):
            size = sum(f.stat().st_size for f in backup.rglob('*') if f.is_file())
            size_mb = size / 1024 / 1024
            file_count = sum(1 for f in backup.rglob('*') if f.is_file())
            print(f"  {i}. {backup.name} ({file_count} Dateien, {size_mb:.2f} MB)")
    else:
        print("üì≠ Keine Backups vorhanden")

def restore_backup(timestamp: str):
    """Stellt ein Backup wieder her"""
    backup_folder = BACKUP_DIR / timestamp
    
    if not backup_folder.exists():
        print(f"‚ùå Backup nicht gefunden: {timestamp}")
        list_recent_backups(10)
        return
    
    print(f"\n‚ö†Ô∏è  ACHTUNG: Alle aktuellen Dateien werden √ºberschrieben!")
    print(f"   Backup: {timestamp}")
    confirm = input(f"\nWiederherstellen? (yes/no): ")
    
    if confirm.lower() != "yes":
        print("‚ùå Abgebrochen")
        return
    
    restored = 0
    for file_path in FILES_TO_BACKUP:
        source = backup_folder / file_path
        dest = BASE_DIR / file_path
        
        if not source.exists():
            continue
        
        # Erstelle Zielordner falls n√∂tig
        dest.parent.mkdir(parents=True, exist_ok=True)
        
        # Kopiere zur√ºck
        shutil.copy2(source, dest)
        print(f"‚úÖ Wiederhergestellt: {file_path}")
        restored += 1
    
    print(f"\n‚úÖ Backup wiederhergestellt: {restored} Dateien")

